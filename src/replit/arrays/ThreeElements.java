package replit.arrays;

public class ThreeElements {
    public static void main(String[] args) {
        int[] nums = new int[3];
        nums[0] = 34;
        nums[1] = 59;
        nums[2] = 19;
        System.out.println("First element " + nums[0]);
        System.out.println("Middle element " + nums[1]);
        System.out.println("Last element " + nums[2]);
    }
}
